package polygons;

public class Point {
    
    private int xCoordinate;
    
    private int yCoordinate;
    
    public Point(int x, int y) {
        
        xCoordinate = x;
        
        yCoordinate = y;
        
    }

    public int getxCoordinate() {
        return xCoordinate;
    }

    public int getyCoordinate() {
        return yCoordinate;
    }
    
}
